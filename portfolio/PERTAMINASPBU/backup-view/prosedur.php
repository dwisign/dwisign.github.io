									<p>Proses Input Data</p>
									<ul>
									<li><b>Langkah 1. Pemilihan Lokasi</b> 
									Dalam proses ini, Calon Mitra memilih lokasi dari pilihan yang tersedia (Provinsi, Kota/Kabupaten, kecamatan, kelurahan, dan keterangan cakupan lokasi). Untuk memilih lokasi dimana SPBU akan didirikan, silakan klik kanan pada peta di dalam area yang berwarna merah dan klik “simpan”. Kemudian lengkapi detail rencana SPBU Anda dan akhiri dengan menekan tombol “Lanjutkan Pendaftaran”.</li>

									<li><b>Langkah 2. Registrasi</b>
									Calon mitra mendaftarkan nama perusahaan, alamat e-mail, dan nomor handphone yang akan digunakan seterusnya untuk memasuki account pendaftaran SPBU yang dimilikinya. Hal ini akan memudahkan Tim Kelayakan untuk mengidentifikasi pemohon dan memungkinkan Calon Mitra untuk menyimpan data sementara dan mengubah data yang ada pada account-nya. Dari proses registrasi online ini, Calon Mitra akan diberi nomor registrasi untuk melakukan pengecekan status aplikasi dan sebagai pengisian data di website. Jangan lupa untuk memberi centang pada Saya telah membaca dan memahami persyaratan dan prosedur pengajuan pendirian SPBU setelah Anda membaca info SPBU dan Prosedur SPBU.</li>

									<li><b>Langkah 3. Aktivasi Akun</b> 
									Calon Mitra akan menerima email yang berisi tombol untuk aktivasi akun dan SMS yang berisi kode aktivasi. Klik tombol “Aktivasi Akun” kemudian masukan kode aktivasi pada kolom yang tersedia untuk melanjutkan proses input data.</li>

									<li><b>Langkah 4. Profil Pengusaha</b>
									Pada langkah ini, Calon Mitra diminta untuk mengisi data profil pengusaha, yang meliputi riwayat pendidikan, pekerjaan, serta kewirausahaan Calon Mitra untuk mengetahui sejauh mana performansi Calon Mitra dalam menjalankan usahanya, baik untuk bisnis sejenis atau yang lainnya serta untuk mengetahui kemampuan finansial pribadi pengusaha. Hal ini kemudian akan diperiksa dalam proses verifikasi dan menjadi data pendukung dalam pertimbangan Tim Kelayakan untuk menganalisis kelayakan bisnis SPBU yang diajukan.

									<li><b>Langkah 5. Profil Perusahaan</b>
									Pada langkah ini, Calon Mitra diminta untuk mengisi data profil perusahaan untuk mengetahui sejauh mana performansi dan kemampuan finansial perusahaan yang didaftarkan. Hal ini kemudian akan diperiksa dalam proses verifikasi dan menjadi data pendukung dalam pertimbangan Tim Kelayakan untuk menganalisis kelayakan bisnis SPBU yang diajukan.</li>

									<li><b>Langkah 6. Tata Letak</b>
									Dalam langkah ini, Calon Mitra diminta untuk mengisi data yang spesifik mengenai lokasi untuk mengetahui bentuk lahan, posisi, kondisi, dan segala sesuatu yang menyangkut ke dalam persyaratan yang harus dipenuhi dari segi lokasi. Dalam pembangunan SPBU, lokasi adalah faktor terpenting yang memiliki bobot pertimbangan terbesar dalam penilaian kelayakan proposal. Apabila ada variabel-variabel yang tidak sesuai dengan persyaratan standar SPBU PT. Pertamina, maka akan didiskualifikasi, kecuali pemohon dapat mengubah variabel sesuai dengan standar yang telah ditentukan.</li>
									</ul>

									<p>
									Setelah Calon Mitra selesai melengkapi seluruh data yang diminta, akan dilakukan verifikasi awal untuk mengonfirmasi data yang telah diisikan. Pengumuman Calon Mitra yang lolos tahap ini dilakukan paling lambat 7 hari kerja sejak periode pendaftaran online ditutup. Jika Calon Mitra telah lolos tahap verifikasi awal, maka akan diverifikasi untuk menyamakan data yang telah diisikan dengan fakta di lapangan. Perlu kami informasikan bahwa Calon Mitra yang lolos ke tahap verifikasi lapangan akan dikenakan biaya verifikasi sesuai dengan aturan Pertamina.</br></br>

									Jika Calon Mitra dinyatakan layak dalam verifikasi lapangan, maka Calon Mitra akan mendapatkan persetujuan kelayakan secara bisnis dari pihak PT. Pertamina. Keputusan mengenai hasil kelayakan bisnis ini merupakan bagian yang terpisah dari izin pembangunan SPBU yang dikeluarkan oleh pemerintah setempat ataupun bentuk-bentuk izin lainnya yang diperlukan.</br></br>

