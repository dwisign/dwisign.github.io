							    	<h3>Prosedur Pendaftaran Online</h3></br>
							    	<p>Prosedur pendaftaran online terdiri dari 2 (dua) tahap, yaitu :</p>
							    	<ol>
										<li>Proses Input Data, dimana pendaftar mengisikan informasi detail mengenai data perusahaan, data pribadi dan lokasi pengajuan SPBU.</li>

										<li>Proses Verifikasi Awal, yang berupa antara lain :
											<p>
											a. Seleksi Kesiapan Finansial, berfungsi untuk menilai kemampuan pengelolaan finansial calon mitra dan kesiapan finansial Calon Mitra.</br>
											b. Seleksi Kesiapan Lahan, berfungsi untuk menilai apakah lahan yang diajukan layak untuk dibangun SPBU atau tidak. Calon Mitra tidak diharuskan untuk memiliki lahan terlebih dahulu. Untuk informasi lebih lengkap dapat dilihat di halaman <a href="http://spbu.pertamina.com/dashboard/info.aspx">Info SPBU</a>.
											</p>
										</li>
