										    <li>Elemen bangunan yang adaptif terhadap iklim dan lingkungan (sirip penangkal sinar matahari, jendela yang menjorok kedalam, dan penggunaan material dan tekstur yang tepat);</li>
										    <li>Desain bangunan SPBU harus disesuaikan dengan bangunan di lingkungan sekitar yang dominan;</li>
										    <li>Arsitektur bangunan sarana pendukung harus terintegrasi dengan bangunan utama;</li>
										    <li>Seluruh fasade bangunan harus mengekspresikan detail dan karakter arsitektur yang konsisten;</li>
										    <li>Variasi bentuk dan garis atap yang menarik;</li>
										    <li>Bangunan harus adaptif terhadap panas matahari dan pantulan sinar matahari dengan merancang sirip penangkal sinar matahari dan jalur pejalan kaki/ trotoar yang tertutup dengan atap;</li>
										    <li>Bangunan dibagi-bagi menjadi komponen yang berskala lebih kecil untuk menghindari bentuk massa yang terlalu besar;</li>
										    <li>Panduan untuk kanopi adalah sebagai berikut:
										    	<ul>
										        	<li>Integrasi antara kanopi tempat pompa bensin dan bangunan diperbolehkan;</li>
										        	<li>Ketinggian ambang kanopi dihitung dari titik terendah kanopi tidak lebih dari 13’9’’. Ketinggian keseluruhan kanopi tidak lebih dari 17’;</li>
										        	<li>Ceiling kanopi tidak harus menggunakan bahan yang bertekstur atau flat, tidak diperbolehkan menggunakan material yang mengkilat atau bisa memantulkan cahaya;</li>
										        	<li>Tidak diperbolehkan menggunakan lampu tabung pada warna logo perusahaan.</li>
										    	</ul>
										    </li>
										    <li>Panduan untuk pump island adalah sebagai berikut:
										    	<ul>
										        	<li>Pump island ini terdiri dari fuel dispenser, refuse container, alat pembayaran otomatis, bollard pengaman, dan peralatan lainnya;</li>
										        	<li>Desain pump island harus terintergrasi dengan struktur lainnya dalam lokasi, yaitu dengan menggunakan warna, material dan detail arsitektur yang harmonis</li>
										        	<li>Minimalisasi warna dari komponen-komponen pump island, termasuk dispenser, bollard dan lain-lain.</li>
										    	</ul>
										     </li>
										    <li>Sirkulasi/jalur masuk dan keluar:
										    	<ul>
										        	<li>Jalan keluar masuk mudah untuk berbelok ke tempat pompa dan ke tempat antrian dekat pompa, mudah pula untuk berbelok pada saat keluar dari tempat pompa tanpa terhalang apa-apa dan jarak pandang yang baik bagi pengemudi pada saat kembali memasuki jalan raya;</li>
										        	<li>Pintu masuk dan keluar dari SPBU tidak boleh saling bersilangan;</li>
										        	<li>Jumlah lajur masuk minimum 2 (dua) lajur;</li>
										        	<li>Lajur keluar minimum 3 (tiga) lajur atau sama dengan lajur pengisian BBM;</li>
										        	<li>Lebar pintu masuk dan keluar minimal 6 m.</li>
										    	</ul>
										    </li>
