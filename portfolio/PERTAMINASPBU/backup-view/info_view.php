							    	<h3>SPBU</h3></br>
							    	<p>
					    				Pada halaman Info SPBU ini, Anda dapat membaca beberapa informasi umum mengenai SPBU, 
					    				mulai dari definisi istilah, persyaratan standar PT. Pertamina untuk sarana dan prasarana, bangunan, 
					    				lokasi dan perijinan umum, serta ketetapan biaya verifikasi.

										SPBU (Stasiun Pengisian Bahan Bakar untuk Umum) merupakan prasarana umum yang disediakan oleh 
										PT. Pertamina untuk masyarakat luas guna memenuhi kebutuhan bahan bakar. Pada umumnya SPBU menjual 
										bahan bakar sejenis premium, solar, pertamax dan pertamax plus. 
					    			</p>
							    </div>

