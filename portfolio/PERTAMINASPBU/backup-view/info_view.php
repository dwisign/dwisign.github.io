							    	<h3>Bentuk Kerjsama</h3></br>
							    	<p><b>CODO (Company Owned Dealer Operated)</b></br>
										  SPBU CODO PT. Pertamina merupakan SPBU sebagai bentuk kerjasama antara 
										  PT. Pertamina dengan pihak-pihak tertentu. Antara lain kerjasama pemanfaatan 
										  lahan milik perusahaan ataupun individu untuk di bangun SPBU PT. Pertamina. 
										  Skema CODO hanya akan diberikan kepada calon SPBU dengan minimal omset yang 
										  ditentukan berdasarkan verifikasi awal. </p>
							    </div>

