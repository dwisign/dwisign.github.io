								    mengisi formulir proposal pembangunan SPBU.</li>

								    <li>Langkah 4. Data Pribadi dan Perusahaan</b></br>
								    Calon Mitra mengisi bagian ini dengan data pribadi dari pemilik perusahaan. Data pribadi yang 
