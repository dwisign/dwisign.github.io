								    Calon Mitra dalam menjalankan usahanya, baik untuk bisnis sejenis atau yang lainnya. Hal ini kemudian 
								    akan diperiksa dalam proses verifikasi dan menjadi data pendukung dalam pertimbangan Tim Kelayakan 
								    untuk menganalisis kelayakan bisnis SPBU yang diajukan.</li>

